CREATE TABLE FoodSpring.Spring_Session (
	PRIMARY_ID CHAR(36) NOT NULL,
	SESSION_ID CHAR(36) NOT NULL,
	CREATION_TIME BIGINT NOT NULL,
	LAST_ACCESS_TIME BIGINT NOT NULL,
	MAX_INACTIVE_INTERVAL INT NOT NULL,
	EXPIRY_TIME BIGINT NOT NULL,
	PRINCIPAL_NAME VARCHAR(100),
	CONSTRAINT Spring_Session_PK PRIMARY KEY (PRIMARY_ID)
);

CREATE UNIQUE INDEX Spring_Session_IX1 ON FoodSpring.Spring_Session (SESSION_ID);
CREATE INDEX Spring_Session_IX2 ON FoodSpring.Spring_Session (EXPIRY_TIME);
CREATE INDEX Spring_Session_IX3 ON FoodSpring.Spring_Session (PRINCIPAL_NAME);

CREATE TABLE FoodSpring.Spring_Session_ATTRIBUTES (
	SESSION_PRIMARY_ID CHAR(36) NOT NULL,
	ATTRIBUTE_NAME VARCHAR(200) NOT NULL,
	ATTRIBUTE_BYTES IMAGE NOT NULL,
	CONSTRAINT Spring_Session_ATTRIBUTES_PK PRIMARY KEY (SESSION_PRIMARY_ID, ATTRIBUTE_NAME),
	CONSTRAINT Spring_Session_ATTRIBUTES_FK FOREIGN KEY (SESSION_PRIMARY_ID) REFERENCES FoodSpring.Spring_Session(PRIMARY_ID) ON DELETE CASCADE
);
